class Cargo(object):

    def __init__(self, name, size_x, size_y, size_z, weight, latitude, longitude, start_date):

        self.name = name
        self.size_x = size_x
        self.size_y = size_y
        self.size_z = size_z
        self.weight = weight
        self.latitude = latitude
        self.longitude = longitude
        self.start_date = start_date
